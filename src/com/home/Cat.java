public void loadA(List<Cbe> list){
    DefaultTableModel dt=(DefaultTableModel) jTable1.getModel();
    dt.setRowCount(0);
    for(Cbe b:list){
        Vector v=new Vector();
        v.add(b.getId());
        v.add(b.getName());
        dt.addRow(v);
        
    }
    
}
public void loadT(){
    Cba r=new Cba();
   List f=r.loadC();
   loadA(f);
   

}

